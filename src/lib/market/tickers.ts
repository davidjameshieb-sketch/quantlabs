// Deduplicate by symbol (NASDAQ-100 may overlap with S&P 500)
const deduplicateBySymbol = (tickers: TickerInfo[]): TickerInfo[] => {
  const seen = new Set<string>();
  return tickers.filter(t => {
    if (seen.has(t.symbol)) return false;
    seen.add(t.symbol);
    return true;
  });
};

